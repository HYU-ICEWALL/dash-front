'use strict';

angular.module('dashApp', ['ui.bootstrap', 'ui.select2', 'ui.validate'])
  .config(function ($routeProvider) {
    $routeProvider
      .when('/', {
        templateUrl: 'views/main.html',
        controller: 'MainCtrl'
      })
      .otherwise({
        redirectTo: '/'
      });
  });
